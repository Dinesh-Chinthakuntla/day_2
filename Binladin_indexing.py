#Printing the indexing in the word BINLADIN
a="BINLADIN"
print("The given word is : ",a)
print("We are finding the index position of middle letter in frontward direction :",a[3])
print("We are finding the index position of middle letter in backward direction :",a[-4])
print("we are finding second occurrence of I(-2) in backward direction : ",a[-2])
print("we are finding second occurrence of I(1) in frontward direction : ",a[1])
print("The indexing of the word BINLADIN is done")


