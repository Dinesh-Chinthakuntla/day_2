#Printing the indexing in the word SONY BRAVIA
a="SONY BRAVIA"
print("The given word is : ",a)
print("We are finding the index position of B(-6) in backward direction :",a[-6])
print("We are finding the index position of space (4) in frontward direction :",a[4])
print("we are finding second occurrence of I(9) in frontward direction : ",a[9])
print("We are finding the index position of last letter using front ward direction : ", a[10])
print("We are finding the index position of first letter by backward direction : ",a[-11])
print("The indexing of the word SONY BRAVIA is done")


